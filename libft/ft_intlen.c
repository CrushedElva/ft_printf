#include "libft.h"

int	ft_intlen(long long d)
{
	int i;

	if (d < 0)
	{
		i = 1;
		d = -d;
	}
	else
		i = 0;
	if (d == 0)
		return (1);
	while (d != 0)
	{
		d /= 10;
		i++;
	}
	return (i);
}
